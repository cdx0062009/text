#import "XTLabel.h"

@implementation XTLabel

-(id)init {

    self = [super init];
    if (self) {
    
        self.backgroundColor = [UIColor clearColor];
    }
    
    return self;
}

-(id)initWithFrame:(CGRect)frame {

    self = [super initWithFrame:frame];
    if (self) {
        
        self.backgroundColor = [UIColor clearColor];
    }
    
    return self;
}

@end
